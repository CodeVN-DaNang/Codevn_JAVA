public class Student {
    int id;
    String name;
    int age;
    String gender;
    boolean isLearning;

    public Student(int id, String name, int age, String gender, boolean isLearning){
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.isLearning = isLearning;
    }
    
}